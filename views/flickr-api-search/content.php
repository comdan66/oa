<p>專案內利用了 <a href='https://www.flickr.com/services/api/' target='_blank'>Flickr API</a> 製作，作品中 JavaScrip 經由 Flickr Tag API 撈出符合關鍵字的照片集，然後再利用瀑布流套件 <a href='http://masonry.desandro.com/' target='_blank'>Masonry</a> 完成瀑布流效果的搜尋結果。</p>
<p>使用方式如下：</p>
<ul>
  <li>於網頁上方的 <b>搜尋列</b> 輸入欲搜尋的圖片 <b>關鍵字</b>。</li>
  <li>多組關鍵字可用 <b>空白鍵</b> 、 <b>逗號</b>(,) 隔開，ex: ptt,正妹。</li>
  <li>關鍵字輸入完畢後，按下 Enter鍵 或按下 <b>搜尋按鈕</b> 即可。</li>
</ul>