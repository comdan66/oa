<p>寫到最後歸納出幾隻不錯的筆。同時此時，就要抉擇筆身！到底要哪一種筆身、樣式好呢？原本來之前，我偏愛透明、有科技感的筆感造型像是<a href='https://www.facebook.com/twsbi.tw/' target='_blank'>三文堂</a>，抑或者金屬材質的筆桿，但再試寫幾次之後，考量預算以及目的等綜合運算完後，最後選擇 SAILOR 寫樂 REGLUS 新青年鋼筆！</p>
<p>寫了將近數十隻的筆，這隻順暢、手感都蠻不錯的，接下來就是顏色的選擇，開始是用藍色試寫的，詢問之下才知道有黑、紅、白的款式，其中黑色的款式有隱隱的閃亮白點，再加上店員推薦，以及泰瑞強勢認為黑色好，所以就決定是它啦！</p>

